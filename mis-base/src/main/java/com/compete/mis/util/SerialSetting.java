package com.compete.mis.util;

public final class SerialSetting {

    private long no;

    private String periodFormat;

    private String format;

    public long getNo() {
        return no;
    }

    public void setNo(final long no) {
        this.no = no;
    }

    public String getPeriodFormat() {
        return periodFormat;
    }

    public void setPeriodFormat(final String periodFormat) {
        this.periodFormat = periodFormat;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(final String format) {
        this.format = format;
    }
}
